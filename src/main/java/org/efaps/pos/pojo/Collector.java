package org.efaps.pos.pojo;

public class Collector
{

    private String label;
    private String key;

    public String getLabel()
    {
        return label;
    }

    public Collector setLabel(final String _label)
    {
        label = _label;
        return this;
    }

    public String getKey()
    {
        return key;
    }

    public Collector setKey(final String _key)
    {
        key = _key;
        return this;
    }
}
